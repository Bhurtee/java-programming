public class TSA {
    private String name;

    private int age;

    private String skinColor;

    private boolean isOld;

    private float accountBalance;

    public TSA(){
        // Empty constructor
    }

    public TSA (String name, int age, String skinColor, boolean isOld, float accountBalance){
        this.accountBalance = accountBalance;
        this.name = name;
        this.age = age;
        this.skinColor = skinColor;
        this.isOld = isOld;

    }


}
